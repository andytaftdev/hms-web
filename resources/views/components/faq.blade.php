<script>
   document.querySelectorAll(".faq-question").forEach((button) => {
    button.addEventListener("click", () => {
        const answer = button.nextElementSibling;
        const icon = button.querySelector("svg");

        if (answer.style.maxHeight) {
            // Close the active FAQ
            answer.style.maxHeight = null;
            icon.classList.add("rotate-180");
            button.classList.remove("text-[#96BF00]");
            button.classList.add("text-white"); // Set text back to white
        } else {
            // Close all other FAQs
            document.querySelectorAll(".faq-answer").forEach((el) => {
                el.style.maxHeight = null;
                el.previousElementSibling.querySelector("svg").classList.add("rotate-180");
                el.previousElementSibling.classList.remove("text-[#96BF00]");
                el.previousElementSibling.classList.add("text-white"); // Set text to white for all others
            });

            // Open the clicked FAQ
            answer.style.maxHeight = answer.scrollHeight + "px";
            icon.classList.remove("rotate-180");
            button.classList.remove("text-white");
            button.classList.add("text-[#96BF00]"); // Set active text to green
        }
    });
});

</script>

